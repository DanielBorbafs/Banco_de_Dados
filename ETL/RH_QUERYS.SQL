USE RH
GO

SELECT * FROM RH_FUNCIONARIOS
GO

/* Informe em qual cidade possui a maior concentração de homens */
SELECT CIDADE, COUNT(*) AS HABITANTES
FROM RH_FUNCIONARIOS
WHERE SEXO = 'M'
GROUP BY CIDADE
ORDER BY HABITANTES DESC;
GO

/* Média salarial dos homems dessa cidade 
RESULTADO =stockholm */

/*CONFIRMANDO */
SELECT SALARIO, CIDADE
FROM RH_FUNCIONARIOS
WHERE CIDADE = 'STOCKHOLM'
GO

SELECT AVG(SALARIO) AS MEDIA_SALARIAL
FROM RH_FUNCIONARIOS
WHERE SEXO = 'M' AND CIDADE = 'STOCKHOLM';
GO
